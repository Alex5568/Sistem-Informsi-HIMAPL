CREATE OR REPLACE FUNCTION "public"."check_event_capacity"() RETURNS "trigger"
    LANGUAGE "plpgsql"
    AS $$
DECLARE
    current_participants INT;
    max_capacity INT;
BEGIN
    -- Ambil maksimal kapasitas (kuota) dari tabel events
    SELECT kuota INTO max_capacity FROM events WHERE id = NEW.event_id;
    
    -- Hitung berapa jumlah peserta aktif (yang tidak CANCELLED)
    SELECT count(*) INTO current_participants 
    FROM event_participants 
    WHERE event_id = NEW.event_id AND status != 'CANCELLED';
    
    -- Tolak query jika sudah melampaui batas
    IF max_capacity IS NOT NULL AND current_participants >= max_capacity THEN
        RAISE EXCEPTION 'Mohon maaf, kuota event ini sudah penuh.';
    END IF;
    
    RETURN NEW;
END;
$$;


